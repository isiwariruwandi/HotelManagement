    private void btnsignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsignupActionPerformed
        // add validations
        String UserName= text1.getText();
        String Createpassword= text2.getText();
        String Confirmpassword = String.valueOf(text3.getPassword());
                
            if(text1.getText().equals(""))
            {
                JOptionPane.showMessageDialog(new JFrame(), "Please enter a User Name!","Message",JOptionPane.ERROR_MESSAGE);
            }
            else if(text2.getText().equals(""))
            {
                JOptionPane.showMessageDialog(new JFrame(), "Please enter a Password!","Message",JOptionPane.ERROR_MESSAGE);
                
            }
            else if(text3.getText().equals(""))
            { 
                JOptionPane.showMessageDialog(new JFrame(), "Please confirm Password!","Message",JOptionPane.ERROR_MESSAGE);
            }
            else if(!Createpassword.equals(Confirmpassword))
            {
                JOptionPane.showMessageDialog(new JFrame(), "Your Password does not match!","Message",JOptionPane.ERROR_MESSAGE);
            }
            else
            {

                try
                {
                       pst = conn.prepareStatement("INSERT INTO user( UserName, Createpassword) VALUES (?,?)");
                       pst.setString(1, UserName);
                       pst.setString(2, Createpassword);
                       pst.executeUpdate();
                       JOptionPane.showMessageDialog(null, "Registered Successfully");
                       
                }
                catch(SQLException e)
                {
                    JOptionPane.showMessageDialog(null, e);
                }
                
        }
           
    }//GEN-LAST:event_btnsignupActionPerformed

